// Función para convertir puntaje bruto a T
export function convertirAT(
  escala: string,
  bruto: number,
  sexo: 'masculino' | 'femenino'
): number {
  const tabla = sexo === 'masculino' ? TABLA_T_MASCULINO : TABLA_T_FEMENINO;
  const tablaEscala = tabla[escala];
  
  if (!tablaEscala) {
    // Si no hay tabla para esta escala, retornar el bruto como aproximación
    return Math.min(120, Math.max(30, 50 + bruto));
  }
  
  // Buscar el valor T más cercano
  const valor = tablaEscala[bruto];
  if (valor !== undefined) {
    return valor;
  }
  
  // Si el bruto está fuera de rango, usar límites
  const keys = Object.keys(tablaEscala).map(Number).sort((a, b) => a - b);
  const minKey = keys[0];
  const maxKey = keys[keys.length - 1];
  
  if (bruto < minKey) {
    return tablaEscala[minKey];
  }
  if (bruto > maxKey) {
    return tablaEscala[maxKey];
  }
  
  // Interpolación lineal para valores intermedios
  let lower = minKey;
  let upper = maxKey;
  
  for (const k of keys) {
    if (k <= bruto) lower = k;
    if (k >= bruto && upper === maxKey) upper = k;
  }
  
  if (lower === upper) {
    return tablaEscala[lower];
  }
  
  const tLower = tablaEscala[lower];
  const tUpper = tablaEscala[upper];
  const ratio = (bruto - lower) / (upper - lower);
  
  return Math.round(tLower + ratio * (tUpper - tLower));
}

